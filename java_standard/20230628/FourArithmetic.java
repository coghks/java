public class FourArithmetic {
    public static void main(String[] args){
      int a,b; //4칙연산 계산기
      a=12;
      b=2;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
    }
}
