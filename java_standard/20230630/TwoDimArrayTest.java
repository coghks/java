public class TwoDimArrayTest {
    public static void main(String[] args) {
        int[][] a=new int[2][4];
        a[0][0]=10;
        a[0][1]=10;
        a[0][2]=10;
        a[0][3]=10;

        a[1][0]=10;
        a[1][1]=10;
        a[1][2]=10;
        a[1][3]=10;

        System.out.println(a[0][0]+"\t"+a[0][1]+"\t"+a[0][2]+"\t"+a[0][3]);
        System.out.println(a[1][0]+"\t"+a[1][1]+"\t"+a[1][2]+"\t"+a[1][3]);

    }
}
